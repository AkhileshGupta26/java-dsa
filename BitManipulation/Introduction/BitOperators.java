package BitManipulation.Introduction;

public class BitOperators {

    public static void main(String[] args) {
        
        int a=18;
        System.out.println(a&a);
        System.out.println(a|a);
        System.out.println(a^a);
        System.out.println(~a);

        int b=20;
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b);

    }
    
}
