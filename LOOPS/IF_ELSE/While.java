package LOOPS.IF_ELSE;

public class While {
    public static void main(String[] args) {
        // while loop is used to execute a block of code repeatedly as long as the condition is true
        // while(condition){
        //     code block to be executed
        // }
        int i = 0;
        while (i < 5) {
            System.out.println(i); // prints numbers from 0 to 4
            i++;
        }
        // do while loop is similar to while loop but it executes the code block at least once before checking the condition
        // do{
        //     code block to be executed    
        
    }
    
}
